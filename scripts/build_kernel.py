def build(route):
    slug = "changeling-p0-gate" if route == "es" else "changeling-p0-r2"
    parts = ['"""changeling Phase 0 gate runs — AUTOGENERATED by '
             'scripts/build_kernel.py; do not edit by hand. See SPEC.md / '
             'PREREG_P0.md in the repo."""']
    for fname in ORDER:
        body = "\n".join(l for l in (PKG / fname).read_text().splitlines()
                         if not INTRA.match(l))
        parts.append(f"\n# ===== changeling/{fname} =====\n{body}")
    parts.append(MAIN.format(route=route))
    out_dir = ROOT / "kaggle" / slug
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "kernel.py").write_text("\n".join(parts))
    (out_dir / "kernel-metadata.json").write_text(json.dumps({
        "id": f"asystemoffields/{slug}",
        "title": slug,
        "code_file": "kernel.py",
        "language": "python",
        "kernel_type": "script",
        "is_private": True,
        "enable_gpu": True,
        "enable_internet": False,
        "dataset_sources": [],
        "competition_sources": [],
        "kernel_sources": [],
    }, indent=2) + "\n")
    print(f"wrote {out_dir}/kernel.py "
          f"({(out_dir / 'kernel.py').stat().st_size} bytes)")


if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument("--route", choices=["es", "ppo"], default="es")
    a = ap.parse_args()
    build(a.route)
